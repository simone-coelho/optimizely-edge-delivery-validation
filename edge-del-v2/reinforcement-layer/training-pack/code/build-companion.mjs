// Builds the companion into two artifacts:
//
//   companion.min.js          — raw minified IIFE. Drop into your
//                               public assets and reference via
//                               <script src="…"> if your CSP forbids
//                               inline scripts.
//
//   companion-source.mjs      — ESM module that exports
//                               COMPANION_SOURCE as a string constant.
//                               Imported by worker-integration.ts and
//                               inlined into the SSR response body.
//
// Run with:
//   npm install --save-dev esbuild
//   node build-companion.mjs

import { build } from 'esbuild';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));

const companion = await build({
  entryPoints: [resolve(here, 'companion.ts')],
  bundle:        true,
  minify:        true,
  format:        'iife',
  target:        ['es2020'],
  platform:      'browser',
  write:         false,
  legalComments: 'none',
  treeShaking:   true,
});

const companionSource = companion.outputFiles[0].text;

writeFileSync(resolve(here, 'companion.min.js'), companionSource);

writeFileSync(
  resolve(here, 'companion-source.mjs'),
  `// Auto-generated by build-companion.mjs. Do not edit by hand.\n` +
  `export const COMPANION_SOURCE = ${JSON.stringify(companionSource)};\n`
);

writeFileSync(
  resolve(here, 'companion-source.d.ts'),
  `export declare const COMPANION_SOURCE: string;\n`
);

const kb = (companionSource.length / 1024).toFixed(2);
console.log(`  companion bundle: ${kb} KB`);
console.log(`  wrote companion.min.js`);
console.log(`  wrote companion-source.mjs (importable from worker-integration.ts)`);
console.log(`  wrote companion-source.d.ts`);
